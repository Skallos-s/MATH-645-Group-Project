function [facedata,facesize] = Dodecahedron(R)

dAngle  = (atan(2) + asin(2/3)) / 2;
dAngle2 = (atan(2) - asin(2/3)) / 2;
sAngle  = pi * 2 / 5;

Rsin  = R * sin(dAngle);
Rcos  = R * cos(dAngle);
R2sin = R * sin(dAngle2);
R2cos = R * cos(dAngle2);

verts = [[Rcos*sin(sAngle * ((0:4)-0.5));
          Rcos*cos(sAngle * ((0:4)-0.5));
          Rsin*ones(1,5)],...
         [R2cos*sin(sAngle * ((0:4)-0.5));
          R2cos*cos(sAngle * ((0:4)-0.5));
          R2sin*ones(1,5)],...
         [R2cos*sin(sAngle * (0:4));
          R2cos*cos(sAngle * (0:4));
         -R2sin*ones(1,5)],...
         [Rcos*sin(sAngle * (0:4));
          Rcos*cos(sAngle * (0:4));
         -Rsin*ones(1,5)]];

faces = [5,4, 3, 2, 1,...
         1,2, 7,11, 6,...
         2,3, 8,12, 7,...
         3,4, 9,13, 8,...
         4,5,10,14, 9,...
         5,1, 6,15,10,...
         19,18,13, 9,14,...
         20,19,14,10,15,...
         16,20,15, 6,11,...
         17,16,11, 7,12,...
         18,17,12, 8,13,...
         16,17,18,19,20],
facedata = solid(verts,faces);
facesize = 5 * ones(1,12);
